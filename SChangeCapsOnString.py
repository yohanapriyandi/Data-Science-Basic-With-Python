'''
Method/fungsi membuat huruf menjadi kapital atau sebaliknya
- .capitalize() -> Mengubah elemen pertama dari string menjadi huruf kapital.
- .lower() -> Mengubah seluruh huruf dalam teks (string) menjadi huruf kecil
- .upper() -> Mengubah seluruh huruf dalam teks (string) menjadi huruf besar
'''
print(">>> Fitur .capitalize()")
string = 'belajar python programming language'
print(string.capitalize())
print(">>> Fitur .lower()")
string1 = 'Belajar Python Programming Language'
print(string1.lower())
print(">>> Fitur .upper()")
string2 = 'Belajar Python Programming Language'
print(string2.upper())